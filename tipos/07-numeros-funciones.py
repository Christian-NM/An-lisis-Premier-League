import math


print(round(1.3))  # Redondea al entero más cercano
print(round(1.7))
print(round(1.5))
print(abs(55))  # Valor absoluto


print(math.ceil(1.1))  # Redondea hacia arriba
print(math.floor(1.99999))  # Redondea hacia abajo
print(math.sqrt(25))  # Raíz cuadrada
print(math.isnan(23))  # Factorial
print(math.pow(10, 3))  # Potencia
print(math.pi)  # Valor de pi
print(math.e)  # Valor de e
print(math.log10(100))  # Logaritmo base 10
print(math.log2(8))  # Logaritmo base 2
