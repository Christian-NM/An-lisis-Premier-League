nombre_curso = "Ultimate Python"
descripcion_curso = """
Este curso es una introducción completa a Python. 
"""

# Imprime el nombre y la descripción del curso
print(nombre_curso, descripcion_curso)

# Imprime la longitud de la cadena nombre_curso
print(len(nombre_curso))

# Imprime el primer carácter de nombre_curso
print(nombre_curso[0])

# Imprime los caracteres del índice 0 al 7 (no incluye el 8)
print(nombre_curso[0:8])

# Imprime los caracteres desde el índice 9 hasta el final
print(nombre_curso[9:])

# Imprime los caracteres desde el inicio hasta el índice 7
print(nombre_curso[:8])

# Imprime toda la cadena nombre_curso
print(nombre_curso[:])
